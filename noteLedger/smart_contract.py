# Create contract instance
contract = web3.eth.contract(address=contract_address, abi=contract_abi)

# Function to interact with contract
def get_note_details(token_id):
    return contract.functions.noteLedger(token_id).call()

def mint_note(sender_address, private_key, uri, royalty):
    nonce = web3.eth.getTransactionCount(sender_address)
    txn = contract.functions.mintNote(uri, royalty).buildTransaction({
        'chainId': 4,  # Rinkeby Testnet ID
        'gas': 2000000,
        'gasPrice': web3.toWei('50', 'gwei'),
        'nonce': nonce
    })
    signed_txn = web3.eth.account.sign_transaction(txn, private_key)
    tx_hash = web3.eth.sendRawTransaction(signed_txn.rawTransaction)
    return tx_hash.hex()
